<template>
  <div class="todo-list">
    <h1>ToDoList</h1>
    <InputText
      v-model="newTodoText"
      placeholder="(例) 30分ランニングを行う"
      v-on:keydown.enter="addItem"
    />
    <ul v-if="todos.length">
      <ToDoListItem
        v-for="todo in todos"
        v-bind:key="todo.id"
        v-bind:todo="todo"
        v-on:click="updateItem"
      />
    </ul>
    <p v-else>ToDoはありません！</p>
  </div>
</template>

<script>
import InputText from '../atoms/InputText';
import ToDoListItem from '../molecules/ToDoListItem';

export default {
  name: 'ToDoList',
  components: {
    InputText,
    ToDoListItem,
  },
  data() {
    return {
      currentId: 1,
      newTodoText: '',
      todos: [],
    };
  },
  methods: {
    addItem() {
      if (!this.newTodoText) {
        alert('ToDoの内容を入力してください。'); // eslint-disable-line no-alert
        return;
      }
      this.todos.push({
        id: this.currentId,
        text: this.newTodoText,
        isDone: false,
      });
      this.currentId += 1;
      this.newTodoText = '';
    },
    updateItem(id, text, isDone) {
      const index = this.todos.findIndex(
        item => item.id === id,
      );
      if (index === -1) {
        throw new Error('Item Not found');
      }
      this.todos.splice(index, 1, {
        id,
        text,
        isDone,
      });
    },
  },
};
</script>

